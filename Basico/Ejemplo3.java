public class Ejemplo3 {
    public static void main(String[] args) {
        // operadores de asignacion agregada.
        // +=, -=, /=, *=, %=
        int numero = 0;
        System.out.println(numero);
        numero +=23;
        numero -= 1;
        System.out.println(numero);
        numero %= 2; 
        System.out.println(numero);
        numero /= 4;
        System.out.println(numero);
    }
}
